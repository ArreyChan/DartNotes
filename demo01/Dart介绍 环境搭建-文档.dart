Dart环境搭建：
    
  要在我们本地开发Dart程序的话首先需要安装Dart Sdk

  官方文档：https://dart.dev/get-dart


      windows(推荐):
      
        https://gekorm.com/dart-windows/

      mac：

        brew tap dart-lang/dart
        
        brew install dart
  
  验证是否安装成功需要在命令行中使用 dart --version 


Dart 开发工具：
  在Vscode中配置Dart。
    
      1、找到vscode插件安装dart

			2、找到vscode插件安装code runner     

      3、出现乱码需要在setting.json文件中配置："code-runner.runInTerminal": true

    
